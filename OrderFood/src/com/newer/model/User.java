package com.newer.model;

public class User {
        private int uid;
        private int utype;
        private String uname;
        private String upwd;
		public int getUid() {
			return uid;
		}
		public void setUid(int uid) {
			this.uid = uid;
		}
		public int getUtype() {
			return utype;
		}
		public void setUtype(int utype) {
			this.utype = utype;
		}
		public String getUname() {
			return uname;
		}
		public void setUname(String uname) {
			this.uname = uname;
		}
		public String getUpwd() {
			return upwd;
		}
		public void setUpwd(String upwd) {
			this.upwd = upwd;
		}
        
}
